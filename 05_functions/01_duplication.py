# Write a function print_order(name, chai_type)
# Call it multiple times with different customers

def print_order(name, chai_type):
    print(f"Customer: {name}, Chai Type: {chai_type}!")




print_order("Alice", "Masala Chai")
print_order("Dipak", "Ginger Chai")
